// Get subjects by grade
export const getSubjectsByGrade = (gradeId) => subjects[gradeId] || [];

// Get subject by grade and subject id
export const getSubjectById = (gradeId, subjectId) => {
  const gradeSubjects = subjects[gradeId] || [];
  return gradeSubjects.find((s) => s.id === subjectId);
};

// Get subject by name
export const getSubjectByName = (gradeId, name) => {
  const gradeSubjects = subjects[gradeId] || [];
  return gradeSubjects.find((s) => s.name === name);
};
